class Faq extends Component {
    constructor(props) {
        super(props);
        this.toggle = this.toggle.bind(this);
        this.state = { collapse: 0 };
    }
    toggle(e) {
        let event = e.target.dataset.event;
        this.setState({ collapse: this.state.collapse === Number(event) ? 0 : Number(event) });
    }
    render() {
        const {collapse} = this.state;
        return (
            <div className="faq col-md-8 offset-md-2">
                {questions.map(result => {
                    return (
                        <Card style={{ marginBottom: '1rem' }} key={result.id}>
                            <CardHeader onClick={this.toggle} data-event={result.id}>
                                {result.question}
                                <small style={{ float: 'right' }}>
                                    <i className="material-icons">keyboard_arrow_down</i>
                                </small>
                            </CardHeader>
                            <Collapse isOpen={collapse === result.id}>
                                <CardBody>
                                    {result.answer}
                                </CardBody>
                            </Collapse>
                        </Card>
                    )
                })}
            </div>
        );
    }
}

export default Faq;